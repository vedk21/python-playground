# In python, we need to have virtual environments so that we can maintain different versions of packages for different projects

# example:
# project 1 is using numpy==4.1.1
# and we need numpy==3.5.6 in project 2
# then we add pipenv or virtualenv in our projects
# by using these virtual environments we can maintain different sets of packages versions according to project requirement, instead of globally installing same package version for all.